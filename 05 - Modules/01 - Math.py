import math

# Karekök hesaplama
sayi = 16
karekok = math.sqrt(sayi)
print("Karekök:", karekok)  # Çıktı: 4.0

# Faktöriyel hesaplama
sayi = 5
faktoriyel = math.factorial(sayi)
print("Faktöriyel:", faktoriyel)  # Çıktı: 120

# Trigonometrik fonksiyonlar
derece = 45
radyan = math.radians(derece)
sinus = math.sin(radyan)
kosinus = math.cos(radyan)
print("Sinüs:", sinus)  # Çıktı: 0.7071067811865475
print("Kosinüs:", kosinus)  # Çıktı: 0.7071067811865476

# Pi sayısının değeri
pi_sayisi = math.pi
print("Pi:", pi_sayisi)  # Çıktı: 3.141592653589793
# Integer'dan Float'a dönüşüm
a = 43
a = float(a)
print(a)  # Çıktı: 43.0

# Float'tan Integer'a dönüşüm
b = 3.14
b = int(b)
print(b)  # Çıktı: 3

# String'den Integer'a dönüşüm
c = "123"
c = int(c)
print(c)  # Çıktı: 123

# String'den Float'a dönüşüm
d = "3.14"
d = float(d)
print(d)  # Çıktı: 3.14

# Integer'dan String'e dönüşüm
e = 123
e = str(e)
print(e)  # Çıktı: '123'

# Float'tan String'e dönüşüm
f = 3.14
f = str(f)
print(f)  # Çıktı: '3.14'
